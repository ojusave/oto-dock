async def notify_request_created(request: dict) -> None:
    """Notify every admin that a new MCP request landed.

    Batched-request rows (``batch_id`` populated by a community-agent install
    cascade) are skipped — the caller will fire a single combined
    notification via :func:`notify_batch_created`. Lone requests (e.g. via
    ``mcps-mcp`` or the Browse drawer) still fire one notification per admin
    per request, the legacy behavior."""
    if request.get("batch_id"):
        return
    admin_subs = await asyncio.to_thread(_list_admin_subs)
    if not admin_subs:
        return
    from services.notifications import notification_manager
    requester = _format_requester(request)
    reason = (request.get("reason") or "").strip()
    reason_line = f"\n\n_Reason:_ {reason}" if reason else ""
    for sub in admin_subs:
        await notification_manager.fire_notification(
            title="MCP request pending",
            body=(
                f"{requester} requested **{request['mcp_name']}** for agent "
                f"**{request['agent_slug']}**. Review in Admin → MCP Requests."
                f"{reason_line}"
            ),
            severity="info",
            scope="user",
            target=sub,
            source="mcp",
            source_id=str(request["id"]),
        )


async def notify_batch_created(
    batch_id: str,
    requester_sub: str,
    target_agent_slug: str,
    template_slug: str,
) -> None:
    """Fire one combined notification per admin for a community-agent
    install batch. Skips the requester if they happen to be an admin (admin
    self-installs auto-approve inline anyway — they don't need to be told).
    """
    from storage import mcp_request_store

    rows = await asyncio.to_thread(mcp_request_store.list_requests_by_batch, batch_id)
    if not rows:
        return
    admin_subs = await asyncio.to_thread(_list_admin_subs)
    admin_subs = [s for s in admin_subs if s != requester_sub]
    if not admin_subs:
        return
    from services.notifications import notification_manager
    requester = _format_requester(rows[0])
    summary_lines = []
    for row in rows:
        reason_blurb = f" — _{row['reason']}_" if (row.get("reason") or "").strip() else ""
        summary_lines.append(f"- **{row['mcp_name']}**{reason_blurb}")
    body = (
        f"{requester} wants to install community agent **{template_slug}** "
        f"as `{target_agent_slug}`. This requires admin action on "
        f"{len(rows)} MCP{'s' if len(rows) != 1 else ''}:\n\n"
        + "\n".join(summary_lines)
        + "\n\nReview in Admin → MCP Requests."
    )
    title = (
        f"Community agent install: {len(rows)} MCP"
        f"{'s' if len(rows) != 1 else ''} need approval"
    )
    for sub in admin_subs:
        await notification_manager.fire_notification(
            title=title,
            body=body,
            severity="info",
            scope="user",
            target=sub,
            source="mcp",
            source_id=f"batch:{batch_id}",
        )


async def _maybe_notify_batch_complete(request: dict) -> None:
    """Called after a request transitions to a terminal state. If the request
    is part of a batch AND every row in the batch is now terminal, fire ONE
    follow-up notification to the requester summarizing the outcome.

    Per-MCP approve/reject notifications are suppressed for batched rows —
    the requester gets a single roll-up instead.
    """
    batch_id = request.get("batch_id")
    if not batch_id:
        return
    from storage import mcp_request_store

    all_done = await asyncio.to_thread(
        mcp_request_store.all_in_batch_terminal, batch_id,
    )
    if not all_done:
        return
    rows = await asyncio.to_thread(
        mcp_request_store.list_requests_by_batch, batch_id,
    )
    if not rows:
        return
    from services.notifications import notification_manager

    requester = rows[0]["requested_by"]
    agent_slug = rows[0]["agent_slug"]
    installed = [r for r in rows if r["status"] == "installed"]
    rejected = [r for r in rows if r["status"] == "rejected"]
    cancelled = [r for r in rows if r["status"] == "cancelled"]

    lines = [f"Your community agent **{agent_slug}** is ready."]
    lines.append(
        f"{len(installed)}/{len(rows)} MCP{'s' if len(rows) != 1 else ''} approved."
    )
    if rejected:
        names = ", ".join(f"**{r['mcp_name']}**" for r in rejected)
        lines.append(f"\nRejected: {names}")
        for r in rejected:
            if (r.get("admin_note") or "").strip():
                lines.append(f"- _{r['mcp_name']}_: {r['admin_note']}")
    if cancelled:
        names = ", ".join(f"**{r['mcp_name']}**" for r in cancelled)
        lines.append(f"\nCancelled: {names}")

    severity = "info" if not (rejected or cancelled) else "warning"
    await notification_manager.fire_notification(
        title=f"Community agent {agent_slug}: setup complete",
        body="\n".join(lines),
        severity=severity,
        scope="user",
        target=requester,
        source="mcp",
        source_id=f"batch:{batch_id}",
    )


def _format_requester(request: dict) -> str:
    """Human-readable requester label for notification bodies.

    Falls back to ``Manager`` when no joined-user columns are present (e.g.
    deleted user account), or to the email when ``name`` is empty.
    """
    name = (request.get("requested_by_name") or "").strip()
    email = (request.get("requested_by_email") or "").strip()
    if name and email:
        return f"**{name}** ({email})"
    if name:
        return f"**{name}**"
    if email:
        return f"**{email}**"
    return "A manager"


async def _notify_request_approved(request: dict) -> None:
    from services.notifications import notification_manager
    await notification_manager.fire_notification(
        title="MCP request approved",
        body=(
            f"**{request['mcp_name']}** is installed and enabled for agent "
            f"**{request['agent_slug']}**."
        ),
        severity="info",
        scope="user",
        target=request["requested_by"],
        source="mcp",
        source_id=str(request["id"]),
    )


async def _notify_request_rejected(request: dict) -> None:
    from services.notifications import notification_manager
    note = request.get("admin_note") or ""
    body = f"Request for **{request['mcp_name']}** on agent **{request['agent_slug']}** was rejected."
    if note:
        body += f"\n\nAdmin note: {note}"
    await notification_manager.fire_notification(
        title="MCP request rejected",
        body=body,
        severity="warning",
        scope="user",
        target=request["requested_by"],
        source="mcp",
        source_id=str(request["id"]),
    )


async def _notify_request_failed(request: dict) -> None:
    from services.notifications import notification_manager
    log_excerpt = (request.get("install_log") or "")[:300]
    body = (
        f"Install failed for **{request['mcp_name']}** on agent "
        f"**{request['agent_slug']}**. The admin can retry from MCP Requests."
    )
    if log_excerpt:
        body += f"\n\n```\n{log_excerpt}\n```"
    # Notify both the requester and admins so neither side has to chase the
    # failure manually.
    targets = {request["requested_by"]}
    admin_subs = await asyncio.to_thread(_list_admin_subs)
    targets.update(admin_subs)
    for sub in targets:
        await notification_manager.fire_notification(
            title="MCP install failed",
            body=body,
            # Platform severity vocabulary is info/success/warning/danger —
            # "error" renders unstyled and plays no sound.
            severity="warning",
            scope="user",
            target=sub,
            source="mcp",
            source_id=str(request["id"]),
        )


def _list_admin_subs() -> list[str]:
    """Return every admin user's ``sub``."""
    from storage.pg import get_conn
    with get_conn() as conn:
        rows = conn.execute(
            "SELECT sub FROM users WHERE role='admin'",
        ).fetchall()
        return [r["sub"] for r in rows]
