async def approve_request(request_id: int, admin_sub: str, admin_note: str = "") -> dict:
    """Approve an assignment request: install if needed + enable for agent.

    Returns the updated request row. Status transitions:

    - ``pending → approved → installing → installed`` on success
    - ``pending → approved → installing → install_failed`` on install error
      (the admin can retry later via the same endpoint).

    The MCP is enabled for the requesting agent (``mcp_store.add_agent_mcp``)
    only after the install completes successfully. Notifies the requester
    once the request resolves.
    """
    from storage import mcp_request_store, mcp_store

    req = await asyncio.to_thread(mcp_request_store.get_request, request_id)
    if req is None:
        raise HTTPException(404, f"Request {request_id} not found")
    if req["status"] not in ("pending", "install_failed"):
        raise HTTPException(
            409,
            f"Cannot approve request in status {req['status']!r}",
        )

    # pending → approved → installing in one shot (avoid two notifications).
    if req["status"] == "pending":
        await asyncio.to_thread(
            mcp_request_store.update_status,
            request_id, "approved", resolved_by=admin_sub, admin_note=admin_note,
        )
    req = await asyncio.to_thread(
        mcp_request_store.update_status, request_id, "installing",
    )

    mcp_name = req["mcp_name"]
    agent_slug = req["agent_slug"]

    install_log = ""
    install_failed_msg: str | None = None
    try:
        # Skip the install when the MCP is already on disk and at the current
        # catalog version — the registry's ``get_manifest`` returns ``None``
        # only when the folder is absent. Serialize on the per-MCP install lock
        # so a concurrent direct admin install of the same MCP can't race this
        # on the install dir; re-check inside the lock (the other install may
        # have just landed it while we waited, in which case we no-op).
        from core.credentials import catalog_install_registry
        async with catalog_install_registry.lock_for(mcp_name):
            if mcp_registry.get_manifest(mcp_name) is None:
                install_result = await install_from_catalog(mcp_name)
                install_log = install_result.get("install_log", "")
            else:
                install_log = "Already installed — skipping fetch + dependency install."
    except HTTPException as exc:
        install_failed_msg = exc.detail if isinstance(exc.detail, str) else str(exc.detail)
    except Exception as exc:
        logger.exception("Unexpected install error for %s", mcp_name)
        install_failed_msg = f"{type(exc).__name__}: {exc}"

    if install_failed_msg:
        updated = await asyncio.to_thread(
            mcp_request_store.update_status,
            request_id, "install_failed",
            install_log=install_failed_msg,
        )
        # ``install_failed`` is non-terminal (admin may retry) — don't fire
        # the batch-complete roll-up here; only the per-request failure
        # notification, which the batched path also wants to receive so the
        # admin sees the error inline.
        await _notify_request_failed(updated)
        return updated

    # Install OK → assign the MCP to the agent. ``add_agent_mcp`` is
    # idempotent so a re-request after a previous installed state is safe.
    await asyncio.to_thread(mcp_store.add_agent_mcp, agent_slug, mcp_name)

    # For explicit-mode MCPs (prometheus, ssh-server, unifi-network, etc.),
    # the manager-enabled flag alone isn't enough — the runtime only
    # delivers env to an agent if at least one ``mcp_instances`` row
    # authorizes them. Make sure that's true before declaring success.
    instance_status, instance_detail = await asyncio.to_thread(
        _ensure_agent_authorized_for_instance_mcp, mcp_name, agent_slug,
    )
    if instance_status == "no_instances":
        # Roll the agent_mcps assignment back so the manager UI doesn't
        # show a non-functional row. The install itself is fine; only
        # per-agent authorization is missing.
        await asyncio.to_thread(mcp_store.remove_agent_mcp, agent_slug, mcp_name)
        admin_guidance = (
            f"MCP '{mcp_name}' requires an instance configuration before it "
            f"can be authorized for an agent. Create an instance via "
            f"Admin → MCP Servers → {mcp_name} → Instances (give it credentials "
            f"and optionally tick 'Assign to all agents'), then retry this "
            f"approval."
        )
        updated = await asyncio.to_thread(
            mcp_request_store.update_status,
            request_id, "install_failed",
            install_log=admin_guidance,
        )
        await _notify_request_failed(updated)
        return updated

    if instance_detail:
        install_log = (install_log + "\n" if install_log else "") + instance_detail

    updated = await asyncio.to_thread(
        mcp_request_store.update_status,
        request_id, "installed",
        resolved_by=admin_sub,
        install_log=install_log,
    )
    # Batched rows: suppress the per-request approve notification — the
    # requester gets a single roll-up via _maybe_notify_batch_complete once
    # every row in the batch reaches a terminal state.
    if updated.get("batch_id"):
        await _maybe_notify_batch_complete(updated)
    else:
        await _notify_request_approved(updated)
    return updated


def _ensure_agent_authorized_for_instance_mcp(
    mcp_name: str, agent_slug: str,
) -> tuple[str, str]:
    """Make sure an explicit-mode MCP authorizes ``agent_slug`` via at least one
    instance. No-op for non-explicit (auto-mode) MCPs.

    Returns (status, human_log) where status is one of:

    - ``"not_applicable"`` — MCP is auto-mode; nothing to do.
    - ``"assigned_to_all"`` — a catch-all instance already authorizes everyone.
    - ``"already_authorized"`` — agent was already in some instance's list.
    - ``"added_to_first"`` — agent was attached to the lowest-id instance
      (matches the runtime precedence in
      :func:`mcp_store.get_instance_for_agent_env_delivery`). Admin can move
      the agent to a different instance via the Instances UI if desired.
    - ``"no_instances"`` — explicit-mode MCP with zero instances. Caller
      should treat this as a soft failure and prompt the admin to create
      one before retrying.

    The ``human_log`` is a one-line description suitable for appending to
    the request's ``install_log``.
    """
    from storage import mcp_store

    manifest = mcp_registry.get_manifest(mcp_name)
    # ``getattr`` with default so test-stub manifests (bare ``object()``)
    # naturally fall through to "not applicable" rather than raising.
    if not manifest or getattr(manifest, "assignment_mode", "auto") != "explicit":
        return ("not_applicable", "")

    instances = mcp_store.get_mcp_instances(mcp_name)
    if not instances:
        return ("no_instances", "")

    if any(i["assigned_to_all"] for i in instances):
        return ("assigned_to_all", f"Authorized via catch-all instance.")

    if any(agent_slug in i["agents"] for i in instances):
        return ("already_authorized", f"Agent already attached to an instance.")

    target = sorted(instances, key=lambda i: i["id"])[0]
    mcp_store.add_agent_to_instance(target["id"], agent_slug)
    return (
        "added_to_first",
        f"Attached agent to instance '{target['instance_name']}' "
        f"(lowest id; admin can reassign via Instances UI).",
    )


async def reject_request(request_id: int, admin_sub: str, admin_note: str = "") -> dict:
    """Reject a pending request; notifies the requester with the admin's note."""
    from storage import mcp_request_store
    req = await asyncio.to_thread(mcp_request_store.get_request, request_id)
    if req is None:
        raise HTTPException(404, f"Request {request_id} not found")
    if req["status"] != "pending":
        raise HTTPException(409, f"Cannot reject request in status {req['status']!r}")

    updated = await asyncio.to_thread(
        mcp_request_store.update_status,
        request_id, "rejected",
        resolved_by=admin_sub, admin_note=admin_note,
    )
    if updated.get("batch_id"):
        await _maybe_notify_batch_complete(updated)
    else:
        await _notify_request_rejected(updated)
    return updated


async def cancel_request(request_id: int, user_sub: str) -> dict:
    """Cancel an open pending request. Only the original requester may cancel."""
    from storage import mcp_request_store
    req = await asyncio.to_thread(mcp_request_store.get_request, request_id)
    if req is None:
        raise HTTPException(404, f"Request {request_id} not found")
    if req["requested_by"] != user_sub:
        raise HTTPException(403, "Only the original requester can cancel")
    if req["status"] != "pending":
        raise HTTPException(409, f"Cannot cancel request in status {req['status']!r}")

    updated = await asyncio.to_thread(
        mcp_request_store.update_status,
        request_id, "cancelled",
        resolved_by=user_sub,
    )
    if updated.get("batch_id"):
        await _maybe_notify_batch_complete(updated)
    return updated


